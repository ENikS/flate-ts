    it("Support for Iterator<T> iterface on Array", function () {
        var iterator: any = fourBytes[Symbol.iterator]();
        assert.equal(0x5b, iterator.next().value);
        assert.equal(0xad, iterator.next().value);
        assert.equal(0x9e, iterator.next().value);
        assert.equal(0xf4, iterator.next().value);
        assert.equal(0x04, iterator.next().value);
        assert.equal(0x20, iterator.next().value);
        assert.isTrue(iterator.next().done);
    });

    it("Support for Iterator<T> iterface on Uint8Array", function () {
        var iterable = new Uint8Array(fourBytes);
        assert.isDefined(iterable);
        var iterator: any = iterable[Symbol.iterator]();
        assert.equal(0x5b, iterator.next().value);
        assert.equal(0xad, iterator.next().value);
        assert.equal(0x9e, iterator.next().value);
        assert.equal(0xf4, iterator.next().value);
        assert.equal(0x04, iterator.next().value);
        assert.equal(0x20, iterator.next().value);
        assert.isTrue(iterator.next().done);
    });
    

